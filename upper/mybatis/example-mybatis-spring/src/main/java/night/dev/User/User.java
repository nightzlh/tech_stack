package night.dev.User;

public class User {
    private int id;
    private String name;

    public int getId() {
        return id;
    }
    public void getId(int id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
}